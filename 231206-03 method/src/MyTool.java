public class MyTool {
	// 메소드 정의(head), 구현(body)
	// 정의 : 반환타입(return) 이름
	// 구현 : 문장(들)
	// void(공허)/ 없음.
	public void doNothing() {
		
	}
	
	public void doReturn() {
		return;
		
//		System.out.println("Hello");
	}
	
	// 메소드는 반환값 있을 수 있음.
	// 반환값이 있는 경우 메소드의 정의 부분에
	// 자료형을 명시해야함.
	public int getOne() {
		return 1;
	}
	
	// 파이 (3.14) 실수값을 반환할 수 있는 메소드
	public double getPI() {
		return 3.14;
	}
}


